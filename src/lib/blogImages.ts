function hashString(str: string): number {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = str.charCodeAt(i) + ((hash << 5) - hash);
  }
  return Math.abs(hash);
}

export function getBlogImage(post: {
  slug: string;
  title: string;
  category?: string;
  image?: string;
}): string {
  const slug = post.slug.toLowerCase();
  
  // 1. Check exact audience matches
  if (audienceImageMap[slug]) {
    return audienceImageMap[slug];
  }

  // 2. Check exact slug mappings
  if (customImageMap[slug]) {
    return customImageMap[slug];
  }

  // 3. If image parameter is already a valid external URL, use it
  if (post.image && (post.image.startsWith("http://") || post.image.startsWith("https://"))) {
    return post.image;
  }

  // 4. Categorize based on category name, slug, or title
  const catSearch = ((post.category || "") + " " + slug + " " + (post.image || "")).toLowerCase();
  
  let targetPool: string[] | undefined;
  if (catSearch.includes("pdf")) targetPool = categoryImagePool.pdf;
  else if (catSearch.includes("image") && (catSearch.includes("editing") || catSearch.includes("edit") || catSearch.includes("resize") || catSearch.includes("crop"))) targetPool = categoryImagePool.image; // editing / resizing uses image pool
  else if (catSearch.includes("image") || catSearch.includes("photo") || catSearch.includes("grayscale") || catSearch.includes("blur")) targetPool = categoryImagePool.image;
  else if (catSearch.includes("dev") || catSearch.includes("code") || catSearch.includes("json") || catSearch.includes("xml") || catSearch.includes("yaml") || catSearch.includes("sql") || catSearch.includes("compiler")) targetPool = categoryImagePool.developer;
  else if (catSearch.includes("calculat") || catSearch.includes("math") || catSearch.includes("emi") || catSearch.includes("sip") || catSearch.includes("interest") || catSearch.includes("percentage") || catSearch.includes("salary") || catSearch.includes("cgpa")) targetPool = categoryImagePool.calculator;
  else if (catSearch.includes("text") || catSearch.includes("word") || catSearch.includes("count") || catSearch.includes("line") || catSearch.includes("case") || catSearch.includes("diff") || catSearch.includes("compare")) targetPool = categoryImagePool.text;
  else if (catSearch.includes("seo") || catSearch.includes("tag") || catSearch.includes("sitemap") || catSearch.includes("robots") || catSearch.includes("heading")) targetPool = categoryImagePool.seo;
  else if (catSearch.includes("qr") || catSearch.includes("scanner")) targetPool = categoryImagePool.qr;
  else if (catSearch.includes("productivity") || catSearch.includes("time") || catSearch.includes("clock") || catSearch.includes("pomodoro")) targetPool = categoryImagePool.productivity;

  // 5. Retrieve deterministic image from pool, or from fallback pool
  if (targetPool && targetPool.length > 0) {
    const idx = hashString(slug) % targetPool.length;
    return targetPool[idx];
  }

  const fallbackIdx = hashString(slug) % fallbackImages.length;
  return fallbackImages[fallbackIdx];
}
